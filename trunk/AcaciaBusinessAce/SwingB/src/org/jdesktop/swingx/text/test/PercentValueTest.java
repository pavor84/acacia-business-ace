    private void onRadioButtonItemStateChanged() {
        if(noneRadioButton.isSelected())
            percentValueField.setEditable(JXPercentValueField.Editable.None);
        else if(allRadioButton.isSelected())
            percentValueField.setEditable(JXPercentValueField.Editable.All);
        else if(percentRadioButton.isSelected())
            percentValueField.setEditable(JXPercentValueField.Editable.Percent);
        else if(valueRadioButton.isSelected())
            percentValueField.setEditable(JXPercentValueField.Editable.Value);

        refresh();
    }

    private void onPercentButtonActionPerformed() {
        BigDecimal percent = percentField.getPercent();
        percentValueField.setPercent(percent);
        refresh();
    }

    private void onValueButtonActionPerformed() {
        // TODO add your handling code here:
        BigDecimal percentValue = (BigDecimal)valueField.getValue();
        percentValueField.setPercentValue(percentValue);
        refresh();
    }

    private void onBaseButtonActionPerformed() {
        // TODO add your handling code here:
        BigDecimal base = (BigDecimal)baseField.getValue();
        percentValueField.setBaseValue(base);
        refresh();
    }

    private void refresh() {
        percentFieldIsFixedCheckBox.setSelected(percentValueField.isFreezePercent());
    }
