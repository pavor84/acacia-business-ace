    private AddressesListRemote formSession;
    private List<Address> addresses;

    @Override
    protected void initData() {
        BindingGroup bindingGroup = new BindingGroup();
        EntityProperties entityProps = getFormSession().getAddressEntityProperties();
        branchesTable.bind(bindingGroup, getAddresses(), entityProps);
        bindingGroup.bind();

        //Selecting the current branch
        branchesTable.setSelectedRowObject(LocalSession.get(LocalSession.BRANCH));
    }

    protected List<Address> getAddresses()
    {
        if(addresses == null)
            addresses = getFormSession().getAddresses(getOrganizationDataObjectId());

        return addresses;
    }

    protected List<City> getCities()
    {
        return getFormSession().getCities();
    }

    protected EntityProperties getAddressEntityProperties()
    {
        return getFormSession().getAddressEntityProperties();
    }

    protected AddressesListRemote getFormSession()
    {
        if(formSession == null) {
            try {
                formSession = InitialContext.doLookup(AddressesListRemote.class.getName());
            } catch(Exception ex) {
                ex.printStackTrace();
            }
        }

        return formSession;
    }

    @Action
    @Override
    public void close() {
        super.close();
    }

    @Action
    public void select() {
        LocalSession.put(LocalSession.BRANCH, branchesTable.getSelectedRowObject());
        getAcaciaSession().setBranch((Address) branchesTable.getSelectedRowObject());
        close();
    }


